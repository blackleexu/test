        return $this->fetch('comment/index');
    }

    public function commentDel(){
        $id=input('param.commentid');
        Db::startTrans();
        $re=Comment::destroy($id);
        if($re){
            Db::commit();
        }else{
            Db::rollback();
        }
        if ($re){
            $date=[
                'status'=>0,
                'msg'=>'删除成功'
            ];
        }else{
            $date=[
                'status'=>1,
                'msg'=>'删除失败'
            ];
        }
        return $date;
    }
